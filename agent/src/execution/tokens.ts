export function resolveToken(symbol: string): string {
  const upper = symbol.toUpperCase();
  if (upper === 'BNB') return 'BNB'; // native coin, no address needed
  const addr = BSC_TOKENS[upper] ?? BSC_TOKENS[symbol];
  if (!addr) throw new Error(`No BSC contract address for token: ${symbol}. Add it to src/execution/tokens.ts`);
  return addr;
}
